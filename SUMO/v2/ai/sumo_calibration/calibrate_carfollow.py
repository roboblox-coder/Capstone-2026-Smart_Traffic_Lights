# Literature-anchored defaults. Each value carries its source so any
# audit can trace why this number and not another. SUMO's own defaults
# sit a hair on the conservative side of these (tau=1.0, sigma=0.5)
# which keeps V1's existing regression intact when we swap in this file.
@dataclass
class KraussParams:
    accel: float = 2.6
    decel: float = 4.5
    tau: float = 1.0
    sigma: float = 0.5
    minGap: float = 2.5
    maxSpeed: float = 22.22  # 50 mph corridor, m/s
    length: float = 5.0
    # ``apparentDecel`` is what other vehicles assume this one can do --
    # SUMO uses it for safety distance calc. Setting it equal to decel
    # eliminates one source of unmodeled timid behavior.
    apparentDecel: Optional[float] = None
    emergencyDecel: float = 9.0
    # Source for each field, used in the XML comment block.
    provenance: dict = None  # type: ignore[assignment]

    def __post_init__(self):
        if self.apparentDecel is None:
            self.apparentDecel = self.decel
        if self.provenance is None:
            self.provenance = {
                "accel": "HCM 6 typical passenger car",
                "decel": "AASHTO Green Book comfortable decel (3.4 m/s^2) "
                         "rounded up to SUMO default 4.5",
                "tau": "Krauss original 1.0s; SUMO default",
                "sigma": "SUMO default driver imperfection (0.5)",
                "minGap": "SUMO default; 2.5m queue gap",
                "maxSpeed": "50 mph corridor speed limit -> 22.22 m/s",
                "length": "passenger car typical 5.0m",
                "apparentDecel": "= decel (eliminates safety overshoot)",
                "emergencyDecel": "SUMO default upper bound 9.0",
            }


# Bounds used by the optional grid search. Wide enough to cover the
# realistic spread of arterial driver behavior; narrow enough to keep
# the search to ~125 combinations.
GRID_TAU = (0.7, 1.0, 1.3)
GRID_SIGMA = (0.3, 0.5, 0.7)
GRID_DECEL = (3.5, 4.5, 5.5)


def write_vtypes_xml(out_path: str, params: KraussParams,
                     fitted_against: Optional[dict] = None) -> None:
    """Write the additional-files-compatible vType block. Provenance
    lives in XML comments so SUMO ignores it without complaint."""
    p = params
    src = p.provenance
    header_lines = [
        '<additional>',
        '  <!-- Generated by ai.sumo_calibration.calibrate_carfollow.',
        f'       UTC timestamp: {_dt.datetime.utcnow().isoformat()}Z',
    ]
    if fitted_against:
        header_lines.append(
            f'       Fitted against: '
            f'{json.dumps(fitted_against, sort_keys=True)}')
    else:
        header_lines.append(
            '       Mode: literature defaults (no empirical fit: '
            'see report.md R3).')
    header_lines.append('       Per-field provenance:')
    for k, v in src.items():
        header_lines.append(f'         {k:<16} = {v}')
    header_lines.append('  -->')

    vtype = (
        f'  <vType id="passenger_calibrated" vClass="passenger" '
        f'carFollowModel="Krauss"\n'
        f'         accel="{p.accel}" decel="{p.decel}" '
        f'apparentDecel="{p.apparentDecel}" '
        f'emergencyDecel="{p.emergencyDecel}"\n'
        f'         tau="{p.tau}" sigma="{p.sigma}" minGap="{p.minGap}" '
        f'maxSpeed="{p.maxSpeed}" length="{p.length}"/>'
    )

    body = "\n".join(header_lines) + "\n" + vtype + "\n</additional>\n"
    Path(out_path).write_text(body, encoding="utf-8")


