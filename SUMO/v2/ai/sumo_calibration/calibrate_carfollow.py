def grid_search_against_target(
    sumo_cfg: str, bottleneck_edge: str, target_flow_vph: float,
) -> tuple:
    """Brute-force best (tau, sigma, decel) against the measured target
    discharge flow. Returns ``(best_params, best_loss, all_scores)``.
    """
    best = None
    scores = []
    for tau in GRID_TAU:
        for sigma in GRID_SIGMA:
            for decel in GRID_DECEL:
                params = KraussParams(tau=tau, sigma=sigma, decel=decel)
                measured = _run_one_sim_and_measure_flow(
                    sumo_cfg, bottleneck_edge, params)
                loss = (measured - target_flow_vph) ** 2
                scores.append({
                    "tau": tau, "sigma": sigma, "decel": decel,
                    "measured_vph": measured, "loss": loss,
                })
                if best is None or loss < best[1]:
                    best = (params, loss)
    return (best[0], best[1], scores)


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawTextHelpFormatter)
    p.add_argument("--out",
                   default="vTypes_calibrated.add.xml")
    p.add_argument("--saturation-flow-target", type=float, default=None,
                   help="Target veh/h/lane to fit against (triggers "
                        "grid search). Requires --bottleneck-edge and "
                        "--sumo-cfg.")
    p.add_argument("--bottleneck-edge", default=None)
    p.add_argument("--sumo-cfg", default="sim_calibrated.sumocfg")
    p.add_argument("--report-out", default=None,
                   help="Optional JSON path to dump the full search "
                        "scores when grid search runs.")
    return p.parse_args()


def main() -> int:
    args = parse_args()

    if args.saturation_flow_target is not None:
        if not args.bottleneck_edge:
            print("ERROR: --saturation-flow-target requires "
                  "--bottleneck-edge.")
            return 2
        print(f"Grid-search calibration: target "
              f"{args.saturation_flow_target} veh/h on "
              f"{args.bottleneck_edge} (cfg={args.sumo_cfg}).")
        try:
            params, loss, scores = grid_search_against_target(
                sumo_cfg=args.sumo_cfg,
                bottleneck_edge=args.bottleneck_edge,
                target_flow_vph=args.saturation_flow_target,
            )
        except NotImplementedError as e:
            print(f"ERROR: {e}")
            return 3
        fitted = {
            "target_vph": args.saturation_flow_target,
            "bottleneck_edge": args.bottleneck_edge,
            "loss": loss,
            "tau": params.tau, "sigma": params.sigma,
            "decel": params.decel,
        }
        write_vtypes_xml(args.out, params, fitted_against=fitted)
        if args.report_out:
            Path(args.report_out).write_text(
                json.dumps({"best": fitted, "all": scores}, indent=2)
                + "\n", encoding="utf-8")
        print(f"Wrote {args.out} (fitted).")
        return 0

    # Default: literature-anchored vTypes.
    params = KraussParams()
    write_vtypes_xml(args.out, params, fitted_against=None)
    print(f"Wrote {args.out} (literature defaults; no empirical fit).")
    print("Run with --saturation-flow-target once sat-flow surveys land "
          "(see report.md R3).")
    return 0


if __name__ == "__main__":
    sys.exit(main())
