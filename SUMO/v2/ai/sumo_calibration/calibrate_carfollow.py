"""Emit a calibrated Krauss vTypes XML; optional grid-search if sat-flow lands.

What this script DOES today: writes ``vTypes_calibrated.add.xml`` with
Krauss tau / sigma / decel pinned to literature midpoints (HCM 6th
Edition + SUMO docs), with every value's source recorded inline as an
XML comment. The point is to take vehicle dynamics off the implicit
"SUMO defaults" footing and onto an explicit, auditable line. The V1
regression still passes on these values because they're within the
band SUMO's own defaults already sit in -- this is not a behavioral
change today, it's a provenance change.

What this script will DO once saturation-flow surveys land: with
``--saturation-flow-target <veh/h/lane>`` and ``--bottleneck-edge <id>``
it runs a small grid search over (tau, sigma, decel), driving SUMO via
TraCI on a one-edge bottleneck, picking the combination that minimises
squared error between simulated steady-state discharge and the target
flow. Results overwrite the literature defaults and update the
provenance attribute. Requires SUMO on PATH.

The plan's original framing -- "fit against real_world_counts.xml
queue-discharge slopes" -- doesn't quite hold: real_world_counts.xml
carries 1-hour entered totals, not discharge slopes (no per-cycle
breakdown survives the routeSampler input format). The grid-search
framework is set up for the right data once City of Bellevue / WSDOT
returns saturation-flow surveys -- see ``report.md`` R3.

Usage (from ``SUMO/v2``):

    # Today, literature defaults:
    python -m ai.sumo_calibration.calibrate_carfollow

    # Once sat-flow data arrives (per-lane veh/h on a free-flow bottleneck):
    python -m ai.sumo_calibration.calibrate_carfollow \\
        --saturation-flow-target 1900 \\
        --bottleneck-edge 513503507 \\
        --sumo-cfg sim_calibrated.sumocfg
"""

from __future__ import annotations

